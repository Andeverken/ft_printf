#include <unistd.h>
#include "libftprintf.h"

void	ft_putnbr(int i)
{
	if (i == -2147483648)
	{
		ft_putnbr(-214748364);
		i = 8;
	}
	if (i < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar(i + 48);
}
