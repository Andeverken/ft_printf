void	check_flags(int *tab, const char *format, int *i)
{
	while (format[*i] == '-' || format[*i] == '+' || format[*i] == ' ' ||
			format[*i] == '#' || format[*i] == '0')
	{
		if (format[*i] == '-' && tab[0] != '-')
			tab[0] = '-';
		if (format[*i] == '+' && tab[1] != '+')
			tab[1] = '+';
		if (format[*i] == ' ' && tab[1] != ' '  && tab[1] != '+')
			tab[1] = ' ' ;
		if (format[*i] == '#' && tab[2] != '#')
			tab[2] = '#';
		if (format[*i] == '0' && tab[0] != '0' && tab[0] != '-')
		{
			tab[0] = '0';
			tab[3] = '0';
		}
		(*i)++;
	}
}
